#include <stdio.h>
int main() {
    int a;  // Declaration of variable 'a'
    a = 10; // Initialization of variable 'a'
    printf("The Value of a is %d\n", a);
// Further variable declarations
    float b; // Declaration of variable 'b'
    b = 5.5; // Initialization of variable 'b'
    printf("The Value of b is %f\n", b);
// Further variable declarations
    char c; // Declaration of variable 'c'
    c = 'A'; // Initialization of variable 'c'
    printf("The Value of c is %c\n", c);
// Further variable declarations
    double d; // Declaration of variable 'd'
    d = 3.14159; // Initialization of variable 'd'
    printf("The Value of d is %lf\n", d);
    return 0;   
}